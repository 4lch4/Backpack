/** A single second of time in milliseconds. */
export const SECOND = 1 * 1000

/** A single minute of time in milliseconds. */
export const MINUTE = 60 * SECOND

/** A single hour of time in milliseconds. */
export const HOUR = 60 * MINUTE

/** A single day of time in milliseconds. */
export const DAY = 24 * HOUR

/** A single week of time in milliseconds. */
export const WEEK = 7 * DAY

/** A single month of time in milliseconds. */
export const MONTH = 30 * DAY

/** A single year of time in milliseconds. */
export const YEAR = 365 * DAY
